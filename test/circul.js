
/*
find the diameter , area and circumference of a circle of radius 20cm.
diameter of circul  D = 2 * r
circumference of a circul c = 2 * PI * r
area of a circul  A = PI * r * r
PI = 3.141
*/
let radius = 20;
const PI = 3.141;
let diameter = 2 * radius;
let circumference = 2 * PI * radius;
let area = PI * radius * radius;


console.log("Diameter " + diameter);
console.log("Circumference  " + circumference );
console.log("Area  " + area );